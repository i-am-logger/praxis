/// Axiom: Injury transitively causes MorphologicalRestoration.
///
/// The complete regeneration cascade: Injury -> ... -> MorphologicalRestoration.
pub struct InjuryCausesRestoration;

impl Axiom for InjuryCausesRestoration {
    fn description(&self) -> &str {
        "injury transitively causes morphological restoration"
    }

    fn holds(&self) -> bool {
        use RegenerationEvent::*;
        let effects = causation::effects_of::<RegenerationCauses>(&Injury);
        effects.contains(&MorphologicalRestoration)
    }
}
pr4xis::register_axiom!(InjuryCausesRestoration);

/// Axiom: BioelectricSignal causes PatternSpecification (Levin's core insight).
///
/// Bioelectric signals (Vmem patterns) encode and specify the target morphology
/// during regeneration. This is the central thesis of Levin's bioelectric code.
pub struct BioelectricCausesPattern;

impl Axiom for BioelectricCausesPattern {
    fn description(&self) -> &str {
        "bioelectric signal causes pattern specification (Levin's core insight)"
    }

    fn holds(&self) -> bool {
        use RegenerationEvent::*;
        let effects = causation::effects_of::<RegenerationCauses>(&BioelectricSignal);
        effects.contains(&PatternSpecification)
    }
}
pr4xis::register_axiom!(BioelectricCausesPattern);

/// Axiom: GapJunctionCommunication causes CollectiveDecision.
///
/// Gap junctions enable tissue-level bioelectric signal propagation,
/// allowing cells to collectively compute the target morphology
/// (Oviedo et al. 2010).
pub struct GapJunctionCausesCollectiveDecision;

impl Axiom for GapJunctionCausesCollectiveDecision {
    fn description(&self) -> &str {
        "gap junction communication causes collective decision"
    }

    fn holds(&self) -> bool {
        use RegenerationEvent::*;
        let effects = causation::effects_of::<RegenerationCauses>(&GapJunctionCommunication);
        effects.contains(&CollectiveDecision)
    }
}
pr4xis::register_axiom!(GapJunctionCausesCollectiveDecision);

/// Axiom: PatternMemory is a PatternConcept, NOT a RegenerationType.
///
/// Pattern memory describes stored morphogenetic information — it is a
/// concept about patterning, not a type of regeneration.
pub struct PatternMemoryIsNotRegenerationType;

impl Axiom for PatternMemoryIsNotRegenerationType {
    fn description(&self) -> &str {
        "PatternMemory is a PatternConcept, not a RegenerationType"
    }

    fn holds(&self) -> bool {
        use RegenerationEntity::*;
        taxonomy::is_a::<RegenerationTaxonomy>(&PatternMemory, &PatternConcept)
            && !taxonomy::is_a::<RegenerationTaxonomy>(&PatternMemory, &RegenerationType)
    }
}
pr4xis::register_axiom!(PatternMemoryIsNotRegenerationType);

/// Axiom: Bistability exists — planarian polarity is a bistable bioelectric state.
///
/// Levin (2015, PMID:26610482) showed that planarian head/tail identity is
/// stored as a bistable Vmem pattern that can be stochastically switched
/// between two stable attractors via gap junction modulation.
pub struct BistabilityIsPatterning;

impl Axiom for BistabilityIsPatterning {
    fn description(&self) -> &str {
        "bistability is a pattern concept and is reversible (Levin 2015)"
    }

    fn holds(&self) -> bool {
        use RegenerationEntity::*;
        taxonomy::is_a::<RegenerationTaxonomy>(&Bistability, &PatternConcept)
            && IsReversible.get(&Bistability) == Some(true)
    }
}
pr4xis::register_axiom!(BistabilityIsPatterning);

/// Axiom: Epimorphic regeneration requires BOTH blastema AND nerve supply.
///
/// Salamander limb regeneration depends on blastema formation and continuous
/// nerve-derived trophic factors (Kumar & Brockes 2012).
pub struct EpimorphicRequiresBlastemaAndNerve;

impl Axiom for EpimorphicRequiresBlastemaAndNerve {
    fn description(&self) -> &str {
        "epimorphic regeneration requires both blastema and nerve supply"
    }

    fn holds(&self) -> bool {
        use RegenerationEntity::*;
        RequiresBlastema.get(&Epimorphic) == Some(true)
            && RequiresNerveSupply.get(&Epimorphic) == Some(true)
    }
}
pr4xis::register_axiom!(EpimorphicRequiresBlastemaAndNerve);

/// Axiom: EpithelialRestitution does NOT require bioelectric signaling.
///
/// Epithelial restitution is a rapid, cell-migration-based wound healing
/// process that does not depend on bioelectric pattern specification.
pub struct EpithelialRestitutionNoBioelectric;

impl Axiom for EpithelialRestitutionNoBioelectric {
    fn description(&self) -> &str {
        "epithelial restitution does not require bioelectric signal"
    }

    fn holds(&self) -> bool {
        use RegenerationEntity::*;
        RequiresBioelectricSignal.get(&EpithelialRestitution) == Some(false)
    }
}
pr4xis::register_axiom!(EpithelialRestitutionNoBioelectric);

/// Axiom: all three body axes (AP, DV, LR) are represented as BodyAxis.
pub struct AllBodyAxesRepresented;

impl Axiom for AllBodyAxesRepresented {
    fn description(&self) -> &str {
        "all three body axes (AP, DV, LR) are represented"
    }

    fn holds(&self) -> bool {
        use RegenerationEntity::*;
        let descendants = taxonomy::descendants::<RegenerationTaxonomy>(&BodyAxis);
        descendants.contains(&AnteriorPosteriorAxis)
            && descendants.contains(&DorsalVentralAxis)
            && descendants.contains(&LeftRightAxis)
            && descendants.len() == 3
    }
}
pr4xis::register_axiom!(AllBodyAxesRepresented);

