//! Regeneration science ontology based on Dr. Michael Levin's research.
//!
//! Models the biology of regeneration — how organisms restore lost or damaged
//! structures — using formal ontological categories, taxonomies, causal graphs,
//! qualities, and axioms.
//!
//! Key scientific facts encoded:
//! - Epimorphic regeneration (salamander limb) requires blastema + nerve supply
//! - Morphallactic regeneration (planarian) remodels without growth
//! - Bioelectric signals specify pattern during regeneration (Levin's core insight)
//! - Gap junction communication enables collective decision-making in tissues
//! - Planarian head/tail polarity is bistable and can be stochastically edited
//!   (Levin 2015, PMID:26610482)
//! - Epithelial restitution is purely cell-migration-based, no bioelectric signal
//!
//! Key references:
//! - Levin 2012: Molecular bioelectricity in developmental biology
//! - Levin 2015: planarian anterior/posterior polarity editing
//! - Kumar & Brockes 2012: nerve dependence in amphibian regeneration
//! - Oviedo et al. 2010: gap junctions in planarian regeneration

