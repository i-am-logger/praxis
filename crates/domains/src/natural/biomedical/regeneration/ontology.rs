/// Events in the regenerative causal chain.
///
/// Models the temporal sequence from injury through restoration, including
/// bioelectric and gap-junction-mediated signaling pathways.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Entity)]
pub enum RegenerationEvent {
    /// Tissue damage initiating the regeneration cascade.
    Injury,
    /// Wound closure by epithelial migration.
    WoundClosure,
    /// Formation of the blastema (proliferative cell mass).
    BlastemaFormation,
    /// Specification of the regeneration pattern (what to build).
    PatternSpecification,
    /// Terminal differentiation of cells into target tissue types.
    Differentiation,
    /// Full restoration of the original morphology.
    MorphologicalRestoration,
    /// Bioelectric signal (Vmem changes) encoding pattern information.
    BioelectricSignal,
    /// Determination of tissue polarity (head vs tail, etc.).
    PolarityDetermination,
    /// Intercellular communication via gap junctions.
    GapJunctionCommunication,
    /// Collective tissue-level decision about target morphology.
    CollectiveDecision,
    /// Nerve-derived signals maintaining blastema growth.
    NerveSignaling,
}

