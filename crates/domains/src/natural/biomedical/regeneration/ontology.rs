/// Quality: does this regeneration type require blastema formation?
///
/// Epimorphic regeneration forms a blastema; morphallactic, compensatory,
/// stem-cell-mediated, and epithelial restitution do not.
#[derive(Debug, Clone)]
pub struct RequiresBlastema;

impl Quality for RequiresBlastema {
    type Individual = RegenerationEntity;
    type Value = bool;

    fn get(&self, individual: &RegenerationEntity) -> Option<bool> {
        use RegenerationEntity::*;
        match individual {
            Epimorphic => Some(true),
            Morphallactic | Compensatory | StemCellMediated | EpithelialRestitution => Some(false),
            _ => None,
        }
    }
}

/// Quality: does this regeneration type require nerve supply?
///
/// Epimorphic regeneration in most organisms depends on nerve-derived factors
/// (Kumar & Brockes 2012). Other types do not.
#[derive(Debug, Clone)]
pub struct RequiresNerveSupply;

impl Quality for RequiresNerveSupply {
    type Individual = RegenerationEntity;
    type Value = bool;

    fn get(&self, individual: &RegenerationEntity) -> Option<bool> {
        use RegenerationEntity::*;
        match individual {
            Epimorphic => Some(true),
            Morphallactic | Compensatory | StemCellMediated | EpithelialRestitution => Some(false),
            _ => None,
        }
    }
}

/// Quality: is the polarity/pattern reversible by bioelectric manipulation?
///
/// Planarian head/tail polarity can be reversed by modulating Vmem via
/// gap junction blockers (Levin 2015, PMID:26610482). This demonstrates
/// that anatomical pattern is stored as a bistable bioelectric state.
#[derive(Debug, Clone)]
pub struct IsReversible;

impl Quality for IsReversible {
    type Individual = RegenerationEntity;
    type Value = bool;

    fn get(&self, individual: &RegenerationEntity) -> Option<bool> {
        use RegenerationEntity::*;
        match individual {
            AnatomicalPolarity | Bistability => Some(true),
            TargetMorphology | PatternMemory => Some(false),
            AnteriorPosteriorAxis | DorsalVentralAxis | LeftRightAxis => Some(true),
            _ => None,
        }
    }
}

/// Characteristic timescale for different regeneration types.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RegenerationTimescale {
    /// Hours (e.g. epithelial restitution in gut).
    Hours,
    /// Days (e.g. planarian head regeneration ~7 days).
    Days,
    /// Weeks (e.g. salamander limb regeneration ~4-8 weeks).
    Weeks,
    /// Months (e.g. liver compensatory regrowth ~2-3 months).
    Months,
}

/// Quality: how fast does this type of regeneration occur?
#[derive(Debug, Clone)]
pub struct RegenerationSpeed;

impl Quality for RegenerationSpeed {
    type Individual = RegenerationEntity;
    type Value = RegenerationTimescale;

    fn get(&self, individual: &RegenerationEntity) -> Option<RegenerationTimescale> {
        use RegenerationEntity::*;
        use RegenerationTimescale::*;
        match individual {
            EpithelialRestitution => Some(Hours),
            Morphallactic => Some(Days),
            Epimorphic | StemCellMediated => Some(Weeks),
            Compensatory => Some(Months),
            _ => None,
        }
    }
}

/// Quality: does this regeneration type require bioelectric signaling?
///
/// Most regeneration types depend on bioelectric signals for pattern
/// specification (Levin's core insight). Epithelial restitution is the
/// exception — it is purely cell-migration-based wound healing.
#[derive(Debug, Clone)]
pub struct RequiresBioelectricSignal;

impl Quality for RequiresBioelectricSignal {
    type Individual = RegenerationEntity;
    type Value = bool;

    fn get(&self, individual: &RegenerationEntity) -> Option<bool> {
        use RegenerationEntity::*;
        match individual {
            Epimorphic | Morphallactic | Compensatory | StemCellMediated => Some(true),
            EpithelialRestitution => Some(false),
            _ => None,
        }
    }
}

