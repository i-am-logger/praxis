/// Every entity in the regeneration ontology.
///
/// Covers regeneration types, pattern concepts, body axes, and structures.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Entity)]
pub enum RegenerationEntity {
    // Regeneration types
    /// Regrowth via blastema formation (e.g. salamander limb).
    Epimorphic,
    /// Remodeling of existing tissue without growth (e.g. planarian).
    Morphallactic,
    /// Organ regrowth by cell proliferation (e.g. liver).
    Compensatory,
    /// Regeneration driven by resident stem cells.
    StemCellMediated,
    /// Rapid wound healing by cell migration (e.g. gut epithelium).
    EpithelialRestitution,

    // Pattern concepts
    /// The target shape the tissue "knows" to regenerate toward.
    TargetMorphology,
    /// Directional information encoded in tissue (head vs tail, etc.).
    AnatomicalPolarity,
    /// Head-to-tail axis.
    AnteriorPosteriorAxis,
    /// Back-to-belly axis.
    DorsalVentralAxis,
    /// Left-right asymmetry axis.
    LeftRightAxis,
    /// Stored morphogenetic information that persists across regeneration cycles.
    PatternMemory,
    /// Two stable attractor states — e.g. planarian head/tail polarity can be
    /// stochastically edited between two stable configurations (Levin 2015).
    Bistability,

    // Structures
    /// Proliferative cell mass at wound site (epimorphic regeneration).
    Blastema,
    /// Epithelial sheet covering wound, signals to underlying tissue.
    WoundEpithelium,
    /// Nerve fibers required for blastema maintenance and growth.
    NerveSupply,

    // Abstract categories
    /// Abstract: a type of regeneration.
    RegenerationType,
    /// Abstract: a body axis.
    BodyAxis,
    /// Abstract: a pattern-related concept.
    PatternConcept,
    /// Abstract: a physical structure involved in regeneration.
    Structure,
}

