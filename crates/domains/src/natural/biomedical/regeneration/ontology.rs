    /// Levin 2017 PMID:28538159: brief bioelectric intervention produces long-term
    /// stochastic editing of anatomy. PatternMemory and Bistability are pattern
    /// concepts, not regeneration types.
    #[test]
    fn test_literature_levin_2017_pattern_memory_bistability_are_pattern_concepts() {
        use RegenerationEntity::*;
        // PatternMemory is a PatternConcept, not a RegenerationType
        assert!(
            taxonomy::is_a::<RegenerationTaxonomy>(&PatternMemory, &PatternConcept),
            "Levin 2017: PatternMemory must be a PatternConcept"
        );
        assert!(
            !taxonomy::is_a::<RegenerationTaxonomy>(&PatternMemory, &RegenerationType),
            "Levin 2017: PatternMemory must NOT be a RegenerationType"
        );
        // Bistability is a PatternConcept, not a RegenerationType
        assert!(
            taxonomy::is_a::<RegenerationTaxonomy>(&Bistability, &PatternConcept),
            "Levin 2017: Bistability must be a PatternConcept"
        );
        assert!(
            !taxonomy::is_a::<RegenerationTaxonomy>(&Bistability, &RegenerationType),
            "Levin 2017: Bistability must NOT be a RegenerationType"
        );
    }

