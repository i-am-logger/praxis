//   -> Differentiation -> MorphologicalRestoration
// - Bioelectric pathway: BioelectricSignal -> PolarityDetermination -> PatternSpecification
// - Gap junction pathway: GapJunctionCommunication -> CollectiveDecision -> PatternSpecification
// - Nerve pathway: NerveSignaling -> BlastemaFormation
