/// Regeneration ontology based on Dr. Michael Levin's research.
///
/// Models the science of biological regeneration — how organisms restore
/// lost or damaged structures — as formal ontology:
/// - Regeneration types: epimorphic, morphallactic, compensatory, stem-cell-mediated
/// - Pattern concepts: target morphology, anatomical polarity, bistability
/// - Causal chains: injury → wound closure → blastema → pattern → restoration
/// - Bioelectric control of regenerative patterning
///
/// Key references:
/// - Levin 2012: Molecular bioelectricity in developmental biology
/// - Levin 2015: planarian head/tail polarity editing (PMID:26610482)
/// - Oviedo et al. 2010: gap junctions in planarian regeneration
/// - Kumar & Brockes 2012: nerve dependence in salamander limb regeneration
